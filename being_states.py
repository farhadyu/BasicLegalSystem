class Spirit():
    wave = True

class Physical(Spirit):
    object = True

class Body(Physical):
    history = True


class Intact(Body):
    metabolism = True

class Identity(Intact):
    preservation  = True

class Perceiver(Identity):
    vision = True


class Instinct(Perceiver):
    memory = True

class Animal(Instinct):
    nature = True

class Human(Animal):
    belief = True


class Adam(Human):
    obligations = True

class Self(Adam):
    acceptance = True

class Buddha(Self):
    shadow = True

